package Interface;

public class Metodos {
	public double soma(double a, double b){
		double total = a + b;
		return total;
	}
	
	public double subtra(double a, double b){
		double total = a - b;
		return total;
	}
	
	public double multi(double a, double b){
		double total = a * b;
		return total;
	}
	
	public double div(double a, double b){
		double total = a / b;
		return total;
	}
}
